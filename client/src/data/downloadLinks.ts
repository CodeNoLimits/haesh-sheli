export const getBooksByCategory = (category: string) => {
  if (category === 'all') return breslovDownloadBooks;
  return breslovDownloadBooks.filter(book => book.category === category);
};

export const getBooksByLanguage = (language: string) => {
  if (language === 'all') return breslovDownloadBooks;
  return breslovDownloadBooks.filter(book => book.language === language);
};

export const searchBooks = (searchTerm: string) => {
  const term = searchTerm.toLowerCase();
  return breslovDownloadBooks.filter(book => 
    book.title.toLowerCase().includes(term) ||
    book.titleEnglish.toLowerCase().includes(term) ||
    book.description.toLowerCase().includes(term) ||
    book.author.toLowerCase().includes(term)
  );
};